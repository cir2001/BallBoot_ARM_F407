//按键初始化函数
void KEY_Init(void)
{
	RCC->AHB1ENR|=1<<4;     //使能PORTE时钟
	GPIO_Set(GPIOE,PIN3|PIN4,GPIO_MODE_IN,0,0,GPIO_PUPD_PU);	//PE3~4设置上拉输入
} 





